package com.taller1.spring_1.model;

public class Roles {
	public int Codr;
	public String nombre;
	public int getCodr() {
		return Codr;
	}
	public void setCodr(int codr) {
		Codr = codr;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	public int estado;
}
